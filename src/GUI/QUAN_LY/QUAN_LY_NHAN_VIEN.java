    private void ShowDataToTable(DefaultTableModel model) {
        QLNHANVIEN_DTO loadDAO = new QLNHANVIEN_DTO();
    

    List<NHANVIEN> listNhanVien = loadDAO.getAllNhanVien();
        List<VaiTro> listVaiTro = loadDAO.getAllTenVaiTro();
//        jScrollPane1.setOpaque(true);
//        jScrollPane1.setVisible(true);
        jTable1.setAutoResizeMode(jTable1.AUTO_RESIZE_ALL_COLUMNS);

        for (NHANVIEN n : listNhanVien) {
             String tenVaiTro="";
             for(VaiTro vt:listVaiTro){
                 if(vt.getId().trim().equalsIgnoreCase(n.getIdVaiTRo().trim())){
                     tenVaiTro=vt.getTenVaiTro();
                     break;
                 }
            }
           // System.out.print("Ten vai tro: "+tenVaiTro+"\n");
            model.addRow(new Object[]{
                n.getMANV(), n.getHoten(), n.getCMND(),
                n.getGioiTinh(),tenVaiTro, n.getDiaChi(),
                n.getSDT()
            });
        }
        for (VaiTro s : listVaiTro) {
            jComboBox2.addItem(s.getTenVaiTro());
        }
        jComboBox1.removeAllItems();
        jComboBox1.addItem("Nam");
        jComboBox1.addItem("Nữ");

    }

