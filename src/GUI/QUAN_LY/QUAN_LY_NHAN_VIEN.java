    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        QLNHANVIEN_DTO qlnv = new QLNHANVIEN_DTO();
        List <VaiTro> listVaiTro = qlnv.getAllTenVaiTro();
        int row = (int) jTable1.getSelectedRow();
        
        String MANV = jTable1.getValueAt(row, 0).toString();
        String tenNhanVien = jTable1.getValueAt(row, 1).toString();
        String CMND = jTable1.getValueAt(row, 2).toString();
        String gioiTinh = jTable1.getValueAt(row, 3).toString();
        String vaiTro = jTable1.getValueAt(row, 4).toString();
        String diaChi = jTable1.getValueAt(row, 5).toString();
        String SDT = jTable1.getValueAt(row, 6).toString();
        for (VaiTro vt:listVaiTro){
            if(vt.getId().equalsIgnoreCase(vaiTro)){
                vaiTro= vt.getTenVaiTro();
            }
        }
        // TODO add your handling code here:
        jTextField1.setText(MANV);
        jTextField2.setText(tenNhanVien);
        jTextField3.setText(CMND);
        jTextField4.setText(diaChi);
        jTextField5.setText(SDT);
        if (gioiTinh.equalsIgnoreCase("Nam")) {
            jComboBox1.setSelectedIndex(0);
        } else if (gioiTinh.equalsIgnoreCase("Nữ")) {
            jComboBox1.setSelectedIndex(1);
        }
        for (int i =0; i<jComboBox2.getItemCount(); i++){
            if(jComboBox2.getSelectedItem().toString().equalsIgnoreCase(vaiTro)){
                jComboBox2.setSelectedIndex(i);
                break;
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

