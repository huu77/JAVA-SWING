        if (!modify.checkForExistanceMANV(MANV, listNhanVien)) {

            Notification panel = new Notification(frame,
                    Notification.Type.WARNING,
                    Notification.Location.TOP_RIGHT,
                    "Không tồn tại mã nhân viên!");
            panel.showNotification();
            return;
        }
        int option = JOptionPane.showConfirmDialog(this,
                "Bạn có muốn thay đổi thông tin?", "Xác nhận",
                JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (option == JOptionPane.YES_OPTION) {
            loadDAO.UpdateTrangThaiXoaNhanVien(MANV);
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            model.fireTableDataChanged();
            ShowDataToTable(model);
            resetAllValue();
            Notification panel = new Notification(frame,
                    Notification.Type.SUCCESS,
                    Notification.Location.TOP_RIGHT,
                    "Đã xóa nhân viên");
            panel.showNotification();
        }

