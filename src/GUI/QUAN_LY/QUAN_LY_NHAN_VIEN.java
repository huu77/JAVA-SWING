    private void jButton4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton4MouseClicked
        QLNHANVIEN_DTO loadDAO = new QLNHANVIEN_DTO();
        JFrame frame = (JFrame) getTopLevelAncestor();
        List<NHANVIEN> listNhanVien = loadDAO.getAllNhanVien();
        NhanVienModify modify = new NhanVienModify();
        String MANV = jTextField1.getText();
        String tenNV = jTextField2.getText();
        String CMND = jTextField3.getText();
        String diaChi = jTextField4.getText();
        String SDT = jTextField5.getText();
        String gioiTinh = jComboBox1.getSelectedItem().toString();

        String tenVaiTro = jComboBox2.getSelectedItem().toString();
        tenVaiTro = modify.chuyenDoiMaVaiTro(tenVaiTro);
        if (!modify.checkForExistanceMANV(MANV, listNhanVien)) {

            Notification panel = new Notification(frame,
                    Notification.Type.WARNING,
                    Notification.Location.TOP_RIGHT,
                    "Khong tim thay ma nhan vien can thay doi");
            panel.showNotification();

            return;
        } else if (!modify.validateCMND(CMND)) {

            Notification panel = new Notification(frame,
                    Notification.Type.WARNING,
                    Notification.Location.TOP_RIGHT,
                    "CMND khong hop le!");
            panel.showNotification();
            return;

        } else if (!modify.validatePhoneNumber(SDT)) {

            Notification panel = new Notification(frame,
                    Notification.Type.WARNING,
                    Notification.Location.TOP_RIGHT,
                    "SDT khong hop le!");
            panel.showNotification();
            System.out.println("SDT khong hop le!");
            return;
        }
        int option = JOptionPane.showConfirmDialog(this, "Bạn có muốn thay đổi thông tin?", "Xác nhận", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (option == JOptionPane.YES_OPTION) {
            NHANVIEN nhanVien = new NHANVIEN(MANV, tenNV, CMND, gioiTinh, tenVaiTro, diaChi, SDT);
            loadDAO.UpdateNhanVien(nhanVien);
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            model.fireTableDataChanged();
            ShowDataToTable(model);
            Notification panel = new Notification(frame,
                    Notification.Type.SUCCESS,
                    Notification.Location.TOP_RIGHT,
                    "Đã chỉnh sửa thông tin");
            panel.showNotification();
        }
    }//GEN-LAST:event_jButton4MouseClicked

